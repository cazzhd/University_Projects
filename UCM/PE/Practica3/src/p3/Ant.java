	HashMap<Integer, Boolean> foodvisited = new HashMap<>(100);
	
	public int timeNoEat = 0;
	private int x;
	public int getX() { return x; };
	private int y;
	public int getY() { return y; };
	private double angle;
	public void setAngle(double a) { angle = a; };
	public double getAngle() { return angle; };
	
	public Ant(int x, int y) {
		this.x = x;
		this.y = y;
		this.angle = 0;

		if(MAP_ANT[this.y][this.x] == 1) {
			foodvisited.put(this.x + this.y * 32, true);
		}
	}
	
	// Next Step
	static double tau = Math.PI * 2;
	public void step(AntMovement mov) {
		switch(mov) {
			case TURN_LEFT: this.angle = (this.angle - Math.PI * 0.5 + tau) % tau; break;
			case TURN_RIGHT: this.angle = (this.angle + Math.PI * 0.5 + tau) % tau;  break;
			case MOVE:
				int dx = (int)Math.cos(this.angle);
				int dy = (int)Math.sin(this.angle);
				this.x = (this.x + dx + 32) % 32;
				this.y = (this.y + dy + 32) % 32;
				
				++timeNoEat;
				if(MAP_ANT[this.y][this.x] == 1) {
					timeNoEat = 0;
					foodvisited.put(this.x + this.y * 32, true);
				}
			break;
		}
	}

	// Returns Numnber of food eaten
	public int eaten() { return foodvisited.size(); };


	// Return next block (0 = nothing, 1 = food)
	public boolean sensor() {
		int nx = this.x + (int)Math.cos(this.angle);
		int ny = this.y + (int)Math.sin(this.angle);

		nx = (nx + 32) % 32;
		ny = (ny + 32) % 32;
		
		return ((MAP_ANT[ny][nx] == 1) && !foodvisited.containsKey(nx + ny * 32));
	};

	// Returns Position of the Ant
	public Pair<Integer, Integer> coords() {
		return new Pair<Integer, Integer>(this.x, this.y);
	};

	// Draw
	final int SIZE = 16;
	final int DSX = +200;
	final int DSY = +50;

	public void draw(Graphics2D ctx) {

		ctx.setColor(Color.RED);
		int ax = x * SIZE + DSX;
		int ay = y * SIZE + DSY;
		ctx.fillRect(ax + 3, ay + 3, SIZE - 6, SIZE - 6);

		// Right Eye
		int rx = (int)(Math.cos(angle - Math.PI / 4) * (SIZE / 2 - 2));
		int ry = (int)(Math.sin(angle - Math.PI / 4) * (SIZE / 2 - 2));
		ctx.setColor(Color.BLACK);

		int eye_size = SIZE / 4;
		ctx.fillRect(
			ax + SIZE / 2 - eye_size / 2 + rx,
			ay + SIZE / 2 - eye_size / 2 + ry,
			eye_size, eye_size
		);

		// Left Eye
		rx = (int)(Math.cos(angle + Math.PI / 4) * (SIZE / 2 - 2));
		ry = (int)(Math.sin(angle + Math.PI / 4) * (SIZE / 2 - 2));
		ctx.setColor(Color.BLACK);
		ctx.fillRect(
			ax + SIZE / 2 - eye_size / 2 + rx,
			ay + SIZE / 2 - eye_size / 2 + ry,
			eye_size, eye_size
		);
	}

